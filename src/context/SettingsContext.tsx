export const SettingsProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
    const [theme, setTheme] = useState<Theme>(() => {
        const stored = localStorage.getItem('theme');
        return (stored === 'dark' || stored === 'light') ? stored : 'light';
    });

    const [language, setLanguageState] = useState<Language>(() => {
        const stored = localStorage.getItem('language');
        return (stored === 'es' || stored === 'en' || stored === 'fr') ? stored : 'es';
    });

    useEffect(() => {
        localStorage.setItem('theme', theme);
        if (theme === 'dark') {
            document.documentElement.classList.add('dark');
        } else {
            document.documentElement.classList.remove('dark');
        }
    }, [theme]);

    useEffect(() => {
        localStorage.setItem('language', language);
    }, [language]);

    const toggleTheme = () => {
        setTheme(prev => prev === 'light' ? 'dark' : 'light');
    };

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
    };

    const [appConfig, setAppConfig] = useState<DynamicConfig | null>(null);

    const refreshConfig = async () => {
        try {
            const apiUrl = import.meta.env.VITE_API_URL ? `${import.meta.env.VITE_API_URL}/api` : 'https://bodipo-business-api.onrender.com/api';
            const res = await fetch(`${apiUrl}/config`);
            if (res.ok) {
                const data = await res.json();
                // Normalize paths for Electron if they are absolute
                const normalizedData = { ...data };
                if (normalizedData.content?.hero) {
                    if (normalizedData.content.hero.heroImage?.startsWith('/')) normalizedData.content.hero.heroImage = `.${normalizedData.content.hero.heroImage}`;
                    if (normalizedData.content.hero.moneyTransferImage?.startsWith('/')) normalizedData.content.hero.moneyTransferImage = `.${normalizedData.content.hero.moneyTransferImage}`;
                }
                // Merge with locally stored overrides (admin edits that the server may not persist)
                let localOverrides: Partial<DynamicConfig> = {};
                try {
                    const stored = localStorage.getItem('bb_dynamic_config');
                    if (stored) localOverrides = JSON.parse(stored);
                } catch (_) { /* ignore */ }
                const mergedWithLocal: DynamicConfig = {
                    ...normalizedData,
                    rates: {
                        ...(normalizedData.rates || {}),
                        ...(localOverrides.rates || {}),
                        air: { ...(normalizedData.rates?.air || {}), ...(localOverrides.rates?.air || {}) },
                        sea: { ...(normalizedData.rates?.sea || {}), ...(localOverrides.rates?.sea || {}) },
                        bulto: { ...(normalizedData.rates?.bulto || {}), ...(localOverrides.rates?.bulto || {}) },
                        exchange: { ...(normalizedData.rates?.exchange || {}), ...(localOverrides.rates?.exchange || {}) },
                    },
                    starRates: { ...(normalizedData.starRates || {}), ...(localOverrides.starRates || {}) },
                };
                setAppConfig(mergedWithLocal);
            }
        } catch (error) {
            console.error('Failed to fetch config', error);
        }
    };

    const updateConfig = async (newConfig: Partial<DynamicConfig>) => {
        try {
            const userStr = localStorage.getItem('user');
            const token = userStr ? JSON.parse(userStr).token : '';

            const apiUrl = import.meta.env.VITE_API_URL ? `${import.meta.env.VITE_API_URL}/api` : 'https://bodipo-business-api.onrender.com/api';
            const res = await fetch(`${apiUrl}/config`, {
                method: 'PUT',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`
                },
                body: JSON.stringify(newConfig)
            });

            if (res.ok) {
                const updated = await res.json();
                // Deep merge: always apply what was sent, in case the API response
                // is missing some keys (e.g. starRates, rates.bulto)
                const merged: DynamicConfig = {
                    ...(appConfig as DynamicConfig),
                    ...updated,
                    rates: {
                        ...(appConfig?.rates || {}),
                        ...(updated?.rates || {}),
                        ...(newConfig?.rates || {}),
                        air: { ...(appConfig?.rates?.air || {}), ...(updated?.rates?.air || {}), ...(newConfig?.rates?.air || {}) },
                        sea: { ...(appConfig?.rates?.sea || {}), ...(updated?.rates?.sea || {}), ...(newConfig?.rates?.sea || {}) },
                        bulto: { ...(appConfig?.rates?.bulto || {}), ...(updated?.rates?.bulto || {}), ...(newConfig?.rates?.bulto || {}) },
                        exchange: { ...(appConfig?.rates?.exchange || {}), ...(updated?.rates?.exchange || {}), ...(newConfig?.rates?.exchange || {}) },
                    },
                    starRates: {
                        ...(appConfig?.starRates || {}),
                        ...(updated?.starRates || {}),
                        ...(newConfig?.starRates || {}),
                    },
                    content: { ...(appConfig?.content || {}), ...(updated?.content || {}), ...(newConfig?.content || {}) },
                    contact: { ...(appConfig?.contact || {}), ...(updated?.contact || {}), ...(newConfig?.contact || {}) },
                    discounts: { ...(appConfig?.discounts || {}), ...(updated?.discounts || {}), ...(newConfig?.discounts || {}) },
                };
                
                // Persist the merged config locally so changes to unmapped API fields survive reloads
                localStorage.setItem('bb_dynamic_config', JSON.stringify(merged));
                
                setAppConfig(merged);
            }
        } catch (error) {
            console.error('Failed to update config', error);
            throw error;
        }
    };

    useEffect(() => {
        refreshConfig();
    }, []);

    const t = (key: string, params?: Record<string, any>) => {
        let text = translations[language][key] || translations['es'][key] || key;
        if (params) {
            Object.entries(params).forEach(([k, v]) => {
                text = text.replace(`{{${k}}}`, String(v));
            });
        }
        return text;
    };

    const value = {
        theme,
        language,
        toggleTheme,
        setLanguage,
        t,
        appConfig,
        refreshConfig,
        updateConfig
    };

    return (
        <SettingsContext.Provider value={value}>
            {children}
        </SettingsContext.Provider>
    );
};

export const useSettings = () => {
    const context = useContext(SettingsContext);
    if (!context) {
        throw new Error('useSettings must be used within a SettingsProvider');
    }
    return context;
};
